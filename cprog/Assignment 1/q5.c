/*
Write a program to display ASCII values for character constants ‘\n’, ‘\r’, ‘\t’, ‘\a’, ‘\b’.
*/

#include<stdio.h>
int main()
{
char n='\n',r = '\r',t = '\t', a = '\a', b= '\b';
printf("ASCII values for following escape sequences are  n : %d, r : %d, t : %d, a : %d, b : %d\n",n,r,t,a,b);

return 0;
}
